function color() {
    var listdiv = document.querySelectorAll(".listdiv");
    for (var i = 0; i < listdiv.length; i++) {
        listdiv[i].addEventListener("mouseover",function() {
            this.style.color = "#00a1d6";
        })
        listdiv[i].addEventListener("mouseout",function() {
            this.style.color = "#111111";
        })
    }
}
color();
